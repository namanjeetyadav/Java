
class product {

    double price;
    String product_name;
    String color;

    void set_price(int arg) {
        price = arg;
    }

    void set_product_name(String arg) {
        product_name = arg;
    }

    void set_color(String arg) {
        color = arg;
    }

    double get_price() {
        return price;
    }

    String get_product_name() {
        return product_name;
    }

    String get_color() {
        return color;
    }

}
