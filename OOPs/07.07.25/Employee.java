
class Employee {

    int empId;
    String name;
    String department;

    void setempId(int arg) {
        empId = arg;
    }

    void setname(String arg) {
        name = arg;
    }

    void setdepartment(String arg) {
        department = arg;
    }

    String getname() {
        return name;
    }
     String getdepartment() {
        return department;
    }
     int getempid() {
        return empId;
    }

}
